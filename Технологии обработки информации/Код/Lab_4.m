%% -------- ИСХОДНЫЕ ДАННЫЕ -------- %%
z = [-1.65];
p = [-2.1 -3.41];
k = 1;

T = 0.01;

h = [-0.48 0.24 0.11 0.055 0.026 0.012];

m_akf = 41; % длина половинки АКФ, М – 1
h_padded = [h(:); zeros(m_akf - length(h), 1)];

%% -------- РАСЧЁТ КОЭФФИЦИЕНТОВ -------- %%
denominator = (1+5.51*T+7.161*T*T);
zd=[(1+1.65*T)/denominator -1/denominator];
pd=[-(2+11.02*T)/denominator 1/denominator];

[b1, a1] = eqtflength(zd, pd);

%% -------- ГЕНЕРАЦИЯ ПОСЛЕДОВАТЕЛЬНОСТИ ПСЕВДОСЛУЧАЙНЫХ -------- %%
n_soob=4096; % длина временного ряда
s_v=rand(1,n_soob); % белый шум (0,1)

%% -------- ПОЛУЧЕНИЕ МОДЕЛЕЙ СЛУЧАЙНОГО СИГНАЛА -------- %%

% h – вектор коэффициентов нерекурсивного фильтра
model_1 = filter(h_padded, 1.0, s_v); % НЦФ (7)
% b_1 и a_1 – вектор коэффициентов рекурсивного фильтра
model_2 = filter(b1, a1, s_v); % РЦФ (8)

%% -------- ОПРЕДЕЛЕНИЕ КОЭФФИЦИЕНТОВ АКФ -------- %%
% model – отсчёты процесса, полученного окрашиванием
% m_akf – половина отсчётов АКФ, смотри формулу (9)
[c_akf, lags] = xcorr(model_2, m_akf,'coeff'); % расчёт АКФ
% c_akf – симметричные коэффициенты диапазона lags
% lags = [– М + 1, 0, М – 1] – ось абсцисс в "попугаях"

%% -------- СРАВНЕНИЕ КОЭФФИЦИЕНТОВ АКФ С НЦФ -------- %%
figure % Здесь отображается симметричная АКФ
stem(lags, c_akf); % в виде отсчётов
hold
% коэффициенты НЦФ, начиная с 1-го,
plot(h,'--g','LineWidth',2); % в центре АКФ
grid on;

% среднее отклонение коэффициентов
otklon = sum(sum(abs(c_akf(m_akf+1:2*m_akf) - h_padded))/m_akf)

%% -------- НАХОЖДЕНИЕ СПЕКТРА БЕЛОГО ШУМА -------- %%
f_s = 1.0/T; % частота Котельникова-Найквиста
delta_f=f_s/(n_soob-1); % шаг по цифровой частоте
% вектор частот для асимметричной картинки
f_asi = [0.0:delta_f:f_s];
% вектор частот для симметричной картинки
f_sim = [-0.5*f_s:delta_f:0.5*f_s];
s_asi=fft(model_2); % вычисление БПФ
s_sim=fftshift(s_asi); % трансформация для симметрии

%% -------- ВИЗУАЛИЗАЦИЯ РЕЗУЛЬТАТА -------- %%
figure % выводятся модули спектров
subplot(2, 1, 1);
stem(f_asi(1:25), abs(s_asi(1:25)), '-k', 'LineWidth', 2, Color="blue");
grid on;
title('Furie Transform Asimetrical');
subplot(2, 1, 2);
stem(f_sim(2023:2073), abs(s_sim(2023:2073)), '-k', 'LineWidth', 2, Color="blue");
grid on;
title('Furie Transform simetrical');

%% -------- ПОСТРОЕНИЕ ДИСКРЕТНЫХ ФУНКЦИЙ ПСМ -------- %%
c_akf_padded = [c_akf(:); zeros(n_soob - length(c_akf), 1)];   % столбец той же длины, что и s_asi
g_asi = fft(c_akf_padded);   % теперь длина 4096
g_sim = fftshift(g_asi);

% ПСМ как модуль спектра АКФ (асимметричный вариант)
PSD_asi = abs(g_asi);

figure;
stem(f_asi, PSD_asi, '.', 'MarkerSize', 4);
xlabel('Частота, Гц');
ylabel('Модуль спектра АКФ (ПСМ)');
title('Спектральная плотность мощности (модель 2)');
grid on;
